class Command(BaseCommand):
    help = "Заполнить БД 16 кейсами из Methodology.md"

    def handle(self, *args, **options):
        for c in CASES:
            case, created = Case.objects.update_or_create(
                case_id=c["case_id"],
                defaults={
                    "dimension": c["dimension"],
                    "order": c["order"],
                    "situation_text": c["situation_text"],
                },
            )
            for num, text, score in c["options"]:
                Option.objects.update_or_create(
                    case=case,
                    number=num,
                    defaults={"text": text, "raw_score": score},
                )
            verb = "создан" if created else "обновлён"
            self.stdout.write(f"  {case.case_id} — {verb}")

        self.stdout.write(self.style.SUCCESS(f"Готово: {len(CASES)} кейсов."))
